let puntaje;
puntaje = 5;
console.log(`Puntaje ${puntaje}`);   

// Esto se ve la diferencia si se pone por consola, primero se incrementa o despues
puntaje++;
console.log(`Puntaje ${puntaje}`);   

++puntaje;
console.log(`Puntaje ${puntaje}`);  

puntaje--;
console.log(`Puntaje ${puntaje}`); 

--puntaje;
console.log(`Puntaje ${puntaje}`); 

puntaje +=3;
console.log(`Puntaje ${puntaje}`); 
puntaje -=3;
console.log(`Puntaje ${puntaje}`); 

